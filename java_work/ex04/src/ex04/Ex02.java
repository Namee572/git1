package ex04;

public class Ex02 {
	public static void main(String[] args) {
		
		boolean a = true;
		boolean b = !a;
		
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		
		int c = 10;
		int d = 20;
		
		System.out.println("c = "+c);

		// d = d+c;
		d += c;
		
		System.out.println("d = "+d);
		
	}

}
